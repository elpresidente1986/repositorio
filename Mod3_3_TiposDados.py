#Ler um número inteiro e indicar se é positivo, negativo ou nulo. 

print("Exercicio 3")

numero = int(input("Introduz um numero: "))

if numero > 0:
    print("O numero introduzido é positivo")
elif numero < 0:
    print("O numero introduzido é negativo")
else:
    print("O numero introduzido é nulo")

print()
