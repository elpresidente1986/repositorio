#Pedir dois números inteiros e indicar se são iguais e indicar se são iguais ou diferentes. 

print("Exercicio 2")

numero_1 = int(input("Introduz um numero: "))
numero_2 = int(input("Introduz outro numero: "))

if numero_1 == numero_2:
    print("Os numeros introduzidos são iguais")
else:
    print("Os numeros introduzidos são diferentes")

